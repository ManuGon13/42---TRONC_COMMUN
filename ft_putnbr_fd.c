#include <stdio.h>
#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	long	ln;

	ln = n;
	if (ln < 0)
	{
		write(fd, "-", 1);
		ln = ln * -1;
	}
	if (ln > 9)
		ft_putnbr_fd(ln / 10, fd);
	write(fd, &"0123456789"[ln % 10], 1);
}
